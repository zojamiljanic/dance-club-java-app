    public void dodajTrening(Trening t) {
        t.setDr(dr);
        TableModelTreninziDnevnog tm = (TableModelTreninziDnevnog) tblTreninzi.getModel();
        int rb = 0;
        
        for (Trening trening : tm.getLista()) {
            rb++;
        }
        
        t.setRb(++rb);
        
        try {
            ClientController.getInstance().addTrening(t);
            tm.refreshTable();
            JOptionPane.showMessageDialog(this, "Uspesno dodat trening.");
        } catch (Exception ex) {
            Logger.getLogger(FormDetaljiDnevnogRasporeda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

