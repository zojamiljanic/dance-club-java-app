    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed

        int result = JOptionPane.showConfirmDialog(this, "Dnevni raaspored je prazan. "
                + "Da li ste sigurni da zelite da "
                + "obrisete ovaj dnevni raspored?", "Konfirmacija", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        if (result == JOptionPane.YES_OPTION) {
            try {
                TableModelTreninziDnevnog tm = (TableModelTreninziDnevnog) tblTreninzi.getModel();
                dr.setListaTreninga((ArrayList<Trening>) tm.getLista());
                ClientController.getInstance().deleteDnevniRaspored(dr);
                MainForm mf = (MainForm) getParent();
                mf.osveziTabelu();
                JOptionPane.showMessageDialog(this, "Uspesno obrisan dnevni raspored!");
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(FormDetaljiDnevnogRasporeda.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_btnObrisiActionPerformed

