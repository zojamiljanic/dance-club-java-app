    private void btnObrisiTreningActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiTreningActionPerformed

        int selectedRow = tblTreninzi.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Niste izabrali nijedan red!");
            return;
        }

        TableModelTreninziDnevnog tm = (TableModelTreninziDnevnog) tblTreninzi.getModel();
        Trening t = tm.getSelectedTrening(selectedRow);

        try {
            ClientController.getInstance().deleteTrening(t);
            tm.refreshTable();
            if(tm.getLista().isEmpty()){
                btnObrisiActionPerformed(evt);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Uspesno obrisan trening.");
            }
        } catch (Exception ex) {
            Logger.getLogger(FormDetaljiDnevnogRasporeda.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnObrisiTreningActionPerformed

