    private void popuniTrenere() {
        try {
            ArrayList<Trener> treneri = ClientController.getInstance().getAllTrener();

            cmbTrener.removeAllItems();

            for (Trener trener : treneri) {
                cmbTrener.addItem(trener);
            }
        } catch (Exception ex) {
            Logger.getLogger(FormNoviDnevniRaspored.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void popuniTipovePlesa() {
        try {
            ArrayList<TipPlesa> tipovi = ClientController.getInstance().getAllTipPlesa();

            cmbTipPlesa.removeAllItems();

            for (TipPlesa tipPlesa : tipovi) {
                cmbTipPlesa.addItem(tipPlesa);
            }
        } catch (Exception ex) {
            Logger.getLogger(FormNoviDnevniRaspored.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
