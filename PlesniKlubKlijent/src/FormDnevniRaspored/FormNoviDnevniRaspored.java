    private void btnDodajAdminaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajAdminaActionPerformed

        TableModelTreninzi tm = (TableModelTreninzi) tblTreninzi.getModel();

        dr.setListaTreninga((ArrayList<Trening>) tm.getLista());

        ArrayList<DnevniRaspored> listaDnevnih;
        try {
            listaDnevnih = ClientController.getInstance().getAllDnevniRaspored();

            for (DnevniRaspored dnevniRaspored : listaDnevnih) {
                if (dnevniRaspored.getDatum().equals(dr.getDatum())) {
                    JOptionPane.showMessageDialog(this, "Vec postoji raspored za taj datum!");
                    return;
                }
            }

            ClientController.getInstance().addDnevniRaspored(dr);
            MainForm mf = (MainForm) getParent();
            mf.osveziTabelu();
            JOptionPane.showMessageDialog(this, "Uspesno dodat raspored.");
            this.dispose();

        } catch (Exception ex) {
            Logger.getLogger(FormNoviDnevniRaspored.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnDodajAdminaActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed

        int selectedRow = tblTreninzi.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Niste izabrali nijedan red!");
            return;
        }

        TableModelTreninzi tm = (TableModelTreninzi) tblTreninzi.getModel();
        tm.obrisiTrening(selectedRow);

    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        String vreme = (String) cmbVreme.getSelectedItem();
        Trener trener = (Trener) cmbTrener.getSelectedItem();
        TipPlesa tipPlesa = (TipPlesa) cmbTipPlesa.getSelectedItem();

        TableModelTreninzi tm = (TableModelTreninzi) tblTreninzi.getModel();
        
        if(tm.trenerVecDrzi(vreme, trener)){
            JOptionPane.showMessageDialog(this, "Trener vec drzi trening u to vreme!");
            return;
        }
        
        Trening t = new Trening(dr, -1, vreme, trener, tipPlesa);

        tm.dodajTrening(t);

    }//GEN-LAST:event_btnDodajActionPerformed

